package Ejercicio4Eclipse;

public class Ejercicio4ApartadoC {

	final static double PI = Math.PI;
	final static int RADIO = 23;
	
	public static void main(String[] args) {
			
		double areaCirculo;
		double longitudCircunferencia;
		
		areaCirculo = PI*RADIO*RADIO;
		longitudCircunferencia = 2*PI*RADIO;
		
		System.out.println("El área del círculo es: " + areaCirculo );
		System.out.println("La longitud de la circunferencia es " + longitudCircunferencia);
		

	}

}
